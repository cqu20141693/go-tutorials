// 根据长宽获取面积
func GetArea(weight int, height int) int {
	return weight * height
}

/*
 Test 开头： 普通单元测试
 testing.T 类型入参
*/
func TestGetArea(t *testing.T) {
	area := GetArea(40, 50)
	if area != 2000 {
		t.Error("测试失败")
	}
}

func BenchmarkGetArea(b *testing.B) {
	for i := 0; i < b.N; i++ {
		GetArea(40, 50)
	}
}

